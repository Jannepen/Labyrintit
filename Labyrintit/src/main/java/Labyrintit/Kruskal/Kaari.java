
package Labyrintit.Kruskal;

public class Kaari {
    private int alku;
    private int loppu;
    
    public Kaari(int alku, int loppu) {
        this.alku = alku;
        this.loppu = loppu;
    }

    public int getAlku() {
        return alku;
    }

    public int getLoppu() {
        return loppu;
    }
    
    
    
}
